/*
2. Operações com Variáveis
a) Declare duas variáveis, idade e anoAtual, e atribua valores numéricos a elas.
b) Calcule o ano de nascimento subtraindo idade de anoAtual.
c) Imprima a mensagem "Você nasceu em [ano de nascimento]".
*/
main() {
  int idade = 38;
  int anoAtual = 2024;
  int anoNascimento = anoAtual - idade;
  print("Seu ano de nascimento é ${anoNascimento}");
}
